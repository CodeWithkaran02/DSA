class prefix {
    public static void main(String[] abc) {
        int A = 20;
        System.out.println(A++);
        System.out.println(--A);
        System.out.println(++A);
        System.out.println(A--);
        System.out.println(A);
        System.out.println(A++);
        System.out.println(--A);
    }
}